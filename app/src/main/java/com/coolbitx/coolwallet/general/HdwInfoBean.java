public class HdwInfoBean {

    private String hdwStatus;
    private String hdwName;
    private String hdwPointer;

    public String getHdwStatus() {
        return hdwStatus;
    }

    public void setHdwStatus(String hdwStatus) {
        this.hdwStatus = hdwStatus;
    }

    public String getHdwName() {
        return hdwName;
    }

    public void setHdwName(String hdwName) {
        this.hdwName = hdwName;
    }

    public String getHdwPointer() {
        return hdwPointer;
    }

    public void setHdwPointer(String hdwPointer) {
        this.hdwPointer = hdwPointer;
    }
}
